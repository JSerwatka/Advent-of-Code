def find_all_moves(coord_list):
    rotation_dict = dict([
        (0, "E"),
        (90, "S"),
        (180, "W"),
        (270, "N")
    ])

    moves_dict = {
        "N": 0,
        "S": 0,
        "E": 0,
        "W": 0,
        "rotation": 0
    }

    for instruction, value in coord_list:
        if instruction in "NSEW":
            moves_dict[instruction] += value
        elif instruction == "F":
            direction = rotation_dict[moves_dict["rotation"]]
            moves_dict[direction] += value
        elif instruction == "L":
            moves_dict["rotation"] -= value
            if moves_dict["rotation"] < 0:
                moves_dict["rotation"] = 360 + moves_dict["rotation"]
        elif instruction == "R":
            moves_dict["rotation"] += value
            if moves_dict["rotation"] >= 360:
                moves_dict["rotation"] -= 360

    return moves_dict

def calulcate_distance(moves_dict):
    north_south = abs(moves_dict["N"] - moves_dict["S"])
    east_west = abs(moves_dict["E"] - moves_dict["W"])

    return north_south + east_west

