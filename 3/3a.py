def find_trees(tree_pattern, right, down):
    # i_idx = row
    # j_idx = element of a row
    counter = 0
    j_idx = 0
    row_len = len(tree_pattern[0])

    for i_idx in range(0, len(tree_pattern), down):
        if tree_pattern[i_idx][j_idx % row_len] == "#":
            counter += 1
        j_idx += right
    
    return counter

